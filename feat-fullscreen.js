/* feat-fullscreen.js — Hero Legends Thai
 * Make the SUMMON / STAGES / ARENA / GUILD screens fill large screens with a
 * cinematic full-bleed background (new 16:9 banner art) + a left scrim panel for
 * content, instead of a small card floating in a dark void.
 *
 * APPROACH (conflict-free, no logic rewrite):
 *   - Inject a `.fs-bg` full-bleed layer (per-screen background image) as the first
 *     child of each of the 4 sections. It persists across the feature modules'
 *     body re-renders because it lives OUTSIDE `.body`.
 *   - Pure-CSS reflow of each screen's existing content into a left column over the
 *     baked-in left scrim, with bigger touch targets for large displays.
 *   - For SUMMON, restyle feat-gacha.js's output (hide the tiny thumbnail, promote
 *     the caption to a banner title, enlarge the ×1/×10 pulls) — no DOM rewrite.
 *
 * Backgrounds generated by assets/gen-banner-art.py from the painterly art:
 *   summon=นางละเวง · stages=พระอภัยมณี · arena=ศรีสุวรรณ · guild=สินสมุทร
 *
 * Loads LAST. Vanilla JS, idempotent. All CSS prefixed `fs-` / scoped to the 4 ids.
 */
(function () {
  'use strict';
  var SCREENS = ['summon', 'stages', 'arena', 'guild'];

  // ---------------------------------------------------------------- CSS
  if (!document.getElementById('fs-style')) {
    var css = [
      // full-bleed background layer (only visible when the screen is .on)
      '.fs-bg{position:absolute;inset:0;z-index:0;background-size:cover;background-position:center right;background-repeat:no-repeat}',
      // left scrim + bottom vignette for legibility (art already carries a soft left fade)
      '.fs-bg::after{content:"";position:absolute;inset:0;background:'
        + 'linear-gradient(90deg,rgba(6,6,12,.92) 0%,rgba(6,6,12,.66) 40%,rgba(6,6,12,.12) 68%,rgba(6,6,12,0) 100%),'
        + 'linear-gradient(0deg,rgba(6,6,12,.85) 0%,rgba(6,6,12,.10) 34%,rgba(6,6,12,0) 60%)}',
      '.fs-summon{background-image:url("portraits/summon-bg.jpg")}',
      '.fs-stages{background-image:url("portraits/stages-bg.jpg")}',
      '.fs-arena{background-image:url("portraits/arena-bg.jpg")}',
      '.fs-guild{background-image:url("portraits/guild-bg.jpg")}',
      // lift the real content above the bg
      '#summon>.topbar,#summon>.body,#stages>.topbar,#stages>.body,'
        + '#arena>.topbar,#arena>.body,#guild>.topbar,#guild>.body{position:relative;z-index:2}',
      // content sits in a left column; the hero art breathes on the right
      '#stages>.body,#arena>.body,#guild>.body{max-width:min(64%,820px)}',
      // give list cards a touch more presence on big screens
      '#stages .cmpg-chap,#arena .glass,#guild .glass{backdrop-filter:blur(3px)}',

      // ---------- SUMMON: turn the void into a real banner screen ----------
      '#summon .body{align-items:flex-start!important;justify-content:center!important;text-align:left!important;'
        + 'max-width:min(56%,640px);padding-left:34px;gap:8px}',
      '#summon .banner{width:100%!important;max-width:560px!important;border:none!important;background:transparent!important;'
        + 'overflow:visible!important;border-radius:0!important}',
      '#summon .banner img{display:none!important}',
      '#summon .banner .cap{position:static!important;background:transparent!important;padding:0!important;text-align:left!important}',
      '#summon .banner .cap b{font-size:clamp(26px,4vw,40px);line-height:1.1;display:block;'
        + 'text-shadow:0 3px 18px rgba(0,0,0,.8),0 0 30px rgba(168,85,247,.45);'
        + 'background:linear-gradient(100deg,#fff,#fcd34d 60%,#f59e0b);-webkit-background-clip:text;background-clip:text;-webkit-text-fill-color:transparent}',
      '#summon .banner .cap span{font-size:13px;color:#e9d5ff!important;display:inline-block;margin-top:8px;'
        + 'text-shadow:0 1px 6px rgba(0,0,0,.9)}',
      '#summon .pulls{justify-content:flex-start!important;margin-top:18px!important;gap:14px}',
      '#summon .pull{padding:15px 34px!important;font-size:16px!important;border-radius:16px}',
      '#summon .pull.ten{box-shadow:0 8px 26px rgba(245,196,81,.4)}',
      '#summon .pull.one.glass{background:rgba(20,20,34,.62)!important;border:1px solid var(--line)!important;backdrop-filter:blur(4px)}',
      '#summon .gac-pitybar{margin:16px 0 4px!important;max-width:420px;height:8px!important;border-radius:5px;'
        + 'background:rgba(0,0,0,.45)!important;overflow:hidden}',
      '#summon .gac-pitytxt{color:#cfcfe6!important;text-shadow:0 1px 5px rgba(0,0,0,.9)}',
      '#summon .rates{max-width:520px;color:#b9b9d4!important;text-shadow:0 1px 5px rgba(0,0,0,.9);margin-top:10px!important}',

      // headings over art stay readable
      '#stages .h2,#arena .h2,#guild .h2{text-shadow:0 2px 10px rgba(0,0,0,.85)}',
    ].join('');
    var s = document.createElement('style');
    s.id = 'fs-style';
    s.textContent = css;
    document.head.appendChild(s);
  }

  // ---------------------------------------------------------------- bg layers
  function ensureBg() {
    SCREENS.forEach(function (id) {
      var sec = document.getElementById(id);
      if (!sec) return;
      // first child only; skip if already present
      var first = sec.firstElementChild;
      if (first && first.classList && first.classList.contains('fs-bg')) return;
      var bg = document.createElement('div');
      bg.className = 'fs-bg fs-' + id;
      sec.insertBefore(bg, sec.firstChild);
    });
  }
  ensureBg();

  // re-ensure on navigation (cheap + idempotent) in case a screen was rebuilt
  if (!window.__fsGoHooked && typeof window.go === 'function') {
    var _go = window.go;
    window.go = function (id) {
      var r = _go.apply(this, arguments);
      try { ensureBg(); } catch (e) {}
      return r;
    };
    window.__fsGoHooked = true;
  }
})();
